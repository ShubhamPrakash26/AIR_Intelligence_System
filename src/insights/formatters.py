"""Output formatters for AIR Editorial Intelligence — Week 11/12.

Converts editorial outputs into human-readable publication formats:

- MarkdownFormatter        : APSA-style Markdown (thematic multi-incident editorial)
- ExcelFormatter           : openpyxl workbook with one sheet per section
- APSANewsletterFormatter  : bundled newsletter Markdown from per-incident APSAArticles

All formatters are stateless.
"""

from __future__ import annotations

import io
from datetime import datetime, timezone
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from src.insights.editorial_models import APSAArticle, EditorialReport, EditorialSection


# ---------------------------------------------------------------------------
# Markdown — APSA-aligned
# ---------------------------------------------------------------------------


class MarkdownFormatter:
    """Render an EditorialReport as a strict APSA-style Markdown document.

    Format matches the APSA newsletter standard:
      - Evocative journalistic title
      - Case vignette / executive narrative (no section header)
      - Flowing thematic sections (bold title + educational prose)
      - Conclusion paragraph
      - Academic references (or incident evidence if no academic refs available)
      - Minimal footer
    """

    def format(self, report: "EditorialReport") -> str:
        """Return a complete APSA-style Markdown string for the given report."""
        now = datetime.now(timezone.utc).strftime("%B %d, %Y")
        lines: list[str] = []

        # ── Title — evocative headline preferred ────────────────────────────
        display_title = report.evocative_title if report.evocative_title else report.title
        lines += [
            f"# {display_title}",
            "",
            f"**Published:** {now}",
            "",
            "---",
            "",
        ]

        # ── Vignette / executive narrative ───────────────────────────────────
        lines += [report.executive_summary, "", "---", ""]

        # ── Thematic sections — flowing prose, no Key Learning blocks ────────
        for section in report.sections:
            lines += [f"**{section.title}**", "", section.narrative, ""]

        # ── Conclusion ───────────────────────────────────────────────────────
        lines += ["---", "", report.conclusion, "", "---", ""]

        # ── References — academic preferred; incident evidence as fallback ───
        if report.clinical_references:
            lines += ["**References**", ""]
            for ref in report.clinical_references:
                lines.append(ref)
            lines.append("")
        elif report.all_citations:
            lines += ["**Supporting Incident Evidence**", ""]
            for i, citation in enumerate(report.all_citations, 1):
                lines.append(f"{i}. {citation}")
            lines.append("")

        # ── Footer ────────────────────────────────────────────────────────────
        lines += [
            "---",
            "",
            f"*Generated by AIR Clinical Incident Intelligence Engine · {now}*",
        ]

        return "\n".join(lines)


